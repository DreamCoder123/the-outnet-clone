function displayData(data){
  data.forEach(function (elem) {
    //console.log (elem.name)
    var box = document.createElement("div");
    var fashion_avatar = document.createElement("img");
    fashion_avatar.setAttribute("src", elem.avatar);
     var  fashion_brand = document.createElement("h3");
    fashion_brand.innerText = elem.brand;
    var fashion_name = document.createElement("p");
    fashion_name.innerText = elem.name;
    var fashion_price = document.createElement("p");
    fashion_price.innerText = elem.price;
    fashion_price.style.color = "red"
    var fashion_offer = document.createElement("p");
    fashion_offer.innerText = elem.offer;
    
    var six=document.createElement("button");
    six.innerText="Click ";
    six.style.cursor="pointer";
    six.addEventListener("click",function(){
        press(elem);
    })
    box.append(fashion_avatar,  fashion_brand, fashion_name , fashion_price, fashion_offer,six);
    document.querySelector("#container").append(box);
    //console.log (elem)


  })
}
